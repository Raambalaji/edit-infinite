      let active = false;
      const scrollers = document.querySelectorAll(".scroller");

      // Add event listeners for each scroller element
      scrollers.forEach((scroller) => {
        scroller.addEventListener("mousedown", function () {
          active = true;
          scroller.classList.add("scrolling");
        });

        // Similar changes for touch events
        scroller.addEventListener("touchstart", function () {
          active = true;
          scroller.classList.add("scrolling");
        });
      });

      // Use a common event handler for mouseup and touchend events
      function deactivateScrolling() {
        active = false;
        scrollers.forEach((scroller) => {
          scroller.classList.remove("scrolling");
        });
      }

      document.body.addEventListener("mouseup", deactivateScrolling);
      document.body.addEventListener("mouseleave", deactivateScrolling);
      document.body.addEventListener("touchend", deactivateScrolling);
      document.body.addEventListener("touchcancel", deactivateScrolling);

      // Event listener for mousemove
      document.body.addEventListener("mousemove", function (e) {
        if (!active) return;
        let x = e.pageX;
        x -= document.querySelector('.wrapper').getBoundingClientRect().left;
        scrollIt(x, e.target);
      });
